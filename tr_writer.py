#!/usr/bin/env python3
"""
Ren'Py Translator - Writer Module
Scrive i file di traduzione in game/tl/<lang>/
"""

from __future__ import annotations
from pathlib import Path
import re

_RE_ESCAPE = re.compile(r'([\\"])')
_RE_HEX = re.compile(r"^#?[0-9a-fA-F]{3,8}$")


def _esc(s: str) -> str:
    return _RE_ESCAPE.sub(r"\\\1", s)


def write_tl_files(
    game_dir: Path,
    lang_code: str,
    items: list,
    translations: dict[str, str],
) -> list[Path]:
    """
    Scrive i file di traduzione in game/tl/<lang_code>/.
    Un file per ogni .rpy sorgente, con formato:
        translate <lang_code> strings:
            # file.rpy:line
            old "..."
            new "..."
    """
    tl_dir = game_dir / "tl" / lang_code
    tl_dir.mkdir(parents=True, exist_ok=True)

    # Raggruppa per file sorgente
    groups: dict[str, list] = {}
    for item in items:
        if item.text in translations and translations[item.text]:
            groups.setdefault(item.file, []).append(item)

    seen = set()
    written = []

    for file_key, file_items in groups.items():
        filename = file_key.replace("game/", "").replace("/", "_")
        if not filename.endswith(".rpy"):
            filename += ".rpy"
        out = tl_dir / filename

        lines = [
            "# Auto-generated by Ren'Py Translator — DO NOT EDIT BY HAND",
            f"translate {lang_code} strings:",
            "",
        ]

        for item in file_items:
            old = item.text.strip()
            new = translations.get(item.text, "").strip()
            if not old or not new or old == new:
                continue
            if _RE_HEX.match(old) or old in seen:
                continue
            seen.add(old)
            lines.append(f"    # {item.file}:{item.line}")
            lines.append(f'    old "{_esc(old)}"')
            lines.append(f'    new "{_esc(new)}"')
            lines.append("")

        out.write_text("\n".join(lines).rstrip() + "\n", encoding="utf-8")
        written.append(out)

    return written
